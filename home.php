</div>
<p style="text-align: justify;">
    <h3>Fuzzy Multi Atribut Decision Making (FMADM) adalah suatu metode yang digunakan untuk 
        mencari alternatif optimal dari sejumlah alternatif dengan kriteria. Inti dari FMADM adalah 
        menentukan nilai bobot untuk setiap atribut, kemudian dilanjutkan dengan proses perangkingan 
        yang menyeleksi alternatif yang sudah diberikan, lalu metode Simple Additive Weighting (SAW) 
        digunakan untuk metode Perangkingan atau pengambilan keputusan dalam pemilihan 
        mahasiswa penerima beasiswa Baznas.</h3>
</p>
